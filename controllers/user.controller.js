import User from "../models/users.model.js";
import { curatePlaylistWithAI } from "../services/ai.service.js";
import { mineTrackPool } from "../services/generator.service.js";
import { refreshSpotifyToken } from "../services/spotify.service.js";
import AppError from "../utils/appError.js";
import catchAsync from "../utils/catchAsync.js";

export const saveTopTracksToPlaylist = catchAsync(async (req , res , next) => {
    // نجيب اليوزر ونتاكد ان عنده ال top tracks
    const user = await User.findById(req.user._id).select("+accessToken +tokenExpiresAt topTracks spotifyId");

    // لو مفيش يوزر او التوب تراكس عنده فاضية 
    if(!user || !user.topTracks || user.topTracks.length === 0){
        return next(new AppError("No top tracks found to save!" , 404));
    }

    // نجددالتوكن لو منتهى
    let token = user.accessToken;
    if(Date.now() > new Date(user.tokenExpiresAt).getTime()){
        token = await refreshSpotifyToken(user)
    }

    const fetchOptions = {
        headers: { 
            'Authorization': `Bearer ${token}`,
            'Content-Type': 'application/json'
        }
    };

    const SPOTIFY_API_URL = "https://api.spotify.com";

    // نجهز اسم ال playlist
    const monthYear = new Date().toLocaleString('en-US', { month: 'long', year: 'numeric' });
    const playlistName = `VibeSync: Top Tracks - ${monthYear}`;
    const playlistDescription = `Your personlized sonic footprint for ${monthYear} , generated by VibeSync`;

    // نكريت ال playlist
    const createRes = await fetch(`${SPOTIFY_API_URL}/v1/me/playlists` , {
        method: "POST",
        headers: fetchOptions.headers,
        body: JSON.stringify({
            name:playlistName,
            description:playlistDescription,
            public:false,
        })
    });

    if(!createRes.ok){
        const error = await createRes.json();
        return next(new AppError(`Failed to create playlist: ${error.error.message}` , createRes.status))
    }

    // لو اتعملت بنجاح نبدا نجهز التراكس اللى هنحطها فى ال playlist
    const playlistData = await createRes.json();
    const newPlaylistId = playlistData.id;
    const playlistUrl = playlistData.external_urls.spotify;
    // نجهزها بالشكل اللى سبوتيفاى بيطلبه
    const trackUris = user.topTracks.filter(id => id).map(id => `spotify:track:${id}`);

    // نبدا نبعت الريكوست علشان نضضيف
    const addTracksRes = await fetch(`${SPOTIFY_API_URL}/v1/playlists/${newPlaylistId}/items` , {
        method: "POST",
        headers: fetchOptions.headers,
        body: JSON.stringify({
            uris: trackUris
        })
    })

    if(!addTracksRes.ok){
        const error = await addTracksRes.json();
        return next(new AppError(`Failed to add tracks to playlist: ${error.error.message}` , addTracksRes.status));
    }

    // لو كل دا تمام يارب يعنى ان شاء الله من غير غلطات
    res.status(200).json({
        status: "success",
        message: "Playlist created successfully",
        data: {
            playlistName,
            playlistUrl
        }
    });
})

// دى الدالة اللى هنستعملها علشان نولد بلاى ليست على حسب اليوزر
export const generateVibePlaylist = catchAsync(async (req , res , next) => {
    // ناخد الداتا من اليوزر الحاجات اللى هو عايزيها
    const {vibe , mixRatio , genres , era , length = 20} = req.body;

    if(!vibe || !mixRatio || !genres || !Array.isArray(genres) || !era){
        return next(new AppError("Please provide all required fields", 400));
    };

    const user = await User.findById(req.user.id).select('+accessToken +tokenExpiresAt');

    if(!user){
        return next(new AppError("User not found", 404));
    }

    // لو التوكن منتهى نجدده
    let token = user.accessToken;
    if(Date.now() > new Date(user.tokenExpiresAt).getTime()){
        token = await refreshSpotifyToken(user);
    }

    // نجمع الاغانى اللى هترجعلنا من الفانكشن بتاعتنا
    const trackPool = await mineTrackPool(vibe , genres , era , token);
    console.log(`Successfully gathered ${trackPool.length} raw tracks from Spotify!`);

    if(trackPool.length < length){
        return next(new AppError(`Couldn't find enough tracks for this specific vibe. Try broadening your criteria.` , 404))
    }

    // هنا بقا هندخل فى تحليل ال AI
    let selectedTrackIds = [];
    const arabicCount = Math.floor((mixRatio.arabic / 100) * length);
    const englishCount = length - arabicCount;

    try {
        selectedTrackIds = await curatePlaylistWithAI(trackPool , vibe , length , arabicCount , englishCount);
        
        // لو رجع عدد اقل او اكثر 
        if(!Array.isArray(selectedTrackIds) || selectedTrackIds.length === 0){
            throw new AppError("AI returned invalid data format")
        }

        selectedTrackIds = selectedTrackIds.slice(0 , length);

        console.log(`AI successfully generated ${selectedTrackIds.length} tracks`)
    } catch (error) {
        console.error("AI Curation failed or pending, using Smart Fallback Algorithm...", error);

        // fallback
        const shuffledPool = trackPool.sort(() => 0.5 - Math.random());
        selectedTrackIds = shuffledPool.slice(0, length).map(t => t.id);
    }

    // نبدا بقا نكريت البلاى ليست فى سبوتيفاى 
    const SPOTIFY_API_URL = "https://api.spotify.com";
    const fetchOptions = {
        headers: { 'Authorization': `Bearer ${token}`, 'Content-Type': 'application/json' }
    };

    // إنشاء القائمة
    const playlistName = `VibeSync: ${vibe} Mix 🔥`;
    const createRes = await fetch(`${SPOTIFY_API_URL}/v1/me/playlists`, {
        method: 'POST',
        headers: fetchOptions.headers,
        body: JSON.stringify({
            name: playlistName,
            description: `A custom ${vibe} playlist generated by VibeSync AI. Ratio: ${mixRatio.arabic}% Arabic, ${mixRatio.english}% English.`,
            public: false
        })
    });

    if (!createRes.ok) {
        const errBody = await createRes.json().catch(() => ({}));
        return next(new AppError(`Spotify playlist creation failed: ${errBody?.error?.message || createRes.status}`, createRes.status));
    }

    const playlistData = await createRes.json();
    const newPlaylistId = playlistData.id;

    // إضافة الأغاني للقائمة (بصيغة URI)
    const trackUris = selectedTrackIds.map(id => `spotify:track:${id}`);
    
    const addTracksRes = await fetch(`${SPOTIFY_API_URL}/v1/playlists/${newPlaylistId}/items`, {
        method: 'POST',
        headers: fetchOptions.headers,
        body: JSON.stringify({ uris: trackUris })
    });

    if (!addTracksRes.ok) {
        const errBody = await addTracksRes.json().catch(() => ({}));
        return next(new AppError(`Failed to add tracks to playlist: ${errBody?.error?.message || addTracksRes.status}`, addTracksRes.status));
    }

    if(!user.generatedVibes){
        user.generatedVibes = [];
    }

    // نحفظ تاريخالانشاءات بتاعت اليوزر عنده
    user.generatedVibes.push({
        playlistId: newPlaylistId,
        playlistName,
        playlistUrl: playlistData.external_urls.spotify,
        vibe,
        genres,
        trackCount: selectedTrackIds.length,
    });

    await user.save({validateBeforeSave: false});

    //  الرد النهائي للفرونت إند
    res.status(200).json({
        status: "success",
        message: "Your vibe has been synced! Playlist created.",
        data: {
            playlistName,
            playlistUrl: playlistData.external_urls.spotify,
            trackCount: selectedTrackIds.length
        }
    });
})